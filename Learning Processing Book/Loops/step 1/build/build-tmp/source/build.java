public class build extends PApplet {

public void setup() {
	size(200, 200);
	background(255);
	
}

public void draw() {
	int y = 0;
	while (y < height) {
		stroke(0);
		line(0, y, width, y);
		y += 10;
}
	
}

  static public void main(String[] passedArgs) {
    String[] appletArgs = new String[] { "build" };
    if (passedArgs != null) {
      PApplet.main(concat(appletArgs, passedArgs));
    } else {
      PApplet.main(appletArgs);
    }
  }
}
